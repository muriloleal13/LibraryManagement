    private void operacaoDeletar(){
        lblOperacao.setText(getOperacao().toUpperCase() + " USUARIO");
        btnOperacao.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/user-remove-sm.png")));
        lblTipo.setVisible(false);
        rbAdm.setVisible(false);
        rbAluno.setVisible(false);
        rbFunc.setVisible(false);
        escondeSenha();
        preencheCampos();
        camposNaoEditaveis();
    }
    
    private void operacaoEditar(){
        btnOperacao.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/user-edit-sm.png")));
        lblOperacao.setText(getOperacao().toUpperCase() + " USUARIO");
        preencheCampos();
    }
    
    private void operacaoVisualizar(){
        btnOperacao.setVisible(false);
        rbAdm.setEnabled(false);
        rbAluno.setEnabled(false);
        rbFunc.setEnabled(false);
        escondeSenha();
        camposNaoEditaveis();
        preencheCampos();
    }
    
    private void escondeSenha(){
        txtSenha.setVisible(false);
        lblSenha.setVisible(false);
        txtSenha1.setVisible(false);
        lblSenha1.setVisible(false);
    }

    private void preencheCampos(){
        txtNome.setText(getUser().getNome());
        txtLogin.setText(getUser().getLogin());
        btnOperacao.setText(getOperacao());
        if(getUser().getTipo().equals("Adm"))
            rbAdm.setSelected(true);
        else if(getUser().getTipo().equals("Func"))
            rbFunc.setSelected(true);
        else
            rbAluno.setSelected(true);
    }
    
    private void camposNaoEditaveis(){
        txtNome.setEditable(false);
        txtLogin.setEditable(false);
    }
    
