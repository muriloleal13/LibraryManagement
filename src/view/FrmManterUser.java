    private void btnOperacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOperacaoActionPerformed
        UsuarioDAO user = new UsuarioDAO();
        if(!getOperacao().equals("Inserir"))
            user.setId(getUser().getId());
        user.setNome(txtNome.getText());
        user.setLogin(txtLogin.getText());
        user.setSenha(new String (txtSenha.getPassword()).trim());
        user.setTipo(btnGroup.getSelection().getActionCommand());
        try {
            if(!confereSenha())
            mensagemSenha();
            else if(getOperacao().equals("Inserir")){
                DAL.getInstancia().insereUsuario(user);
                mensagemSucesso("Cadastro");
            }else if(getOperacao().equals("Editar")){
                DAL.getInstancia().editaUsuario(user);
                mensagemSucesso("Edição");
            }else if(getOperacao().equals("Excluir")){
                DAL.getInstancia().removeGeral(Integer.toString(user.getId()), "usuario");
                mensagemSucesso("Exclusão");
            }
            this.dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            mensagemErro();
        }
    }//GEN-LAST:event_btnOperacaoActionPerformed

    private void mensagemSucesso(String operacao){
        JOptionPane.showMessageDialog(this, operacao + " de " + txtNome.getText() + " realizado com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void mensagemErro(){
        JOptionPane.showMessageDialog(this, "Operação não pode ser efetuada", "Aviso", JOptionPane.WARNING_MESSAGE);
    }
    
    private void mensagemSenha(){
        JOptionPane.showMessageDialog(this, "As senhas devem ser iguais", "Aviso", JOptionPane.WARNING_MESSAGE);
    }
    
    private Boolean confereSenha(){
        String primeira = new String (txtSenha.getPassword()).trim();
        String segunda = new String (txtSenha1.getPassword()).trim();
        if(!primeira.equals(segunda))
            return false;
        else
            return true;       
    }
  
