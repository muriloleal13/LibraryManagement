    private void arrumaBotoes(){
        if(!getUsuario().getTipo().equals("Adm")){
            if(getUsuario().getTipo().equals("Func")){                
                btnIncluiUser.setEnabled(false);
                btnAlteraUser.setEnabled(false);
                btnExcluiUser.setEnabled(false);
            }else{
                btnIncluiLivro.setEnabled(false);
                btnAlteraLivro.setEnabled(false);
                btnExcluiLivro.setEnabled(false);
                btnIncluiUser.setEnabled(false);
                btnAlteraUser.setEnabled(false);
                btnExcluiUser.setEnabled(false);
                btnPesquisaUser.setEnabled(false);
                btnListaUser.setEnabled(false);
                btnMultasUser.setEnabled(false);
            }
        }
    }

